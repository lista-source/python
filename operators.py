# Comparison Operators
age1 = 34
age2 = 41
print(age1>age2)
print(age2>age1)
print(age1==age2)
print(age1!=age2)
# Logical Operators
english = 76
swahili = 64
maths = 70
computer = 90
print(english>swahili and maths<computer)
print(english<swahili and maths>computer)
print(english>swahili and maths>computer)
print(english>swahili or maths<computer)
print(english<swahili or maths>computer)
print(not(english>swahili or maths>computer))
